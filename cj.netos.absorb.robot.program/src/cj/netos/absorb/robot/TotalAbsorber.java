package cj.netos.absorb.robot;

import java.math.BigDecimal;

public class TotalAbsorber {
    BigDecimal price;
    long count;

    public BigDecimal getPrice() {
        return price;
    }

    public void setPrice(BigDecimal price) {
        this.price = price;
    }

    public long getCount() {
        return count;
    }

    public void setCount(long count) {
        this.count = count;
    }
}
